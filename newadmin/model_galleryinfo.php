	// Export data in HTML/CSV/Word/Excel/Email/PDF format
	function ExportDocument(&$Doc, &$Recordset, $StartRec, $StopRec, $ExportPageType = "") {
		if (!$Recordset || !$Doc)
			return;
		if (!$Doc->ExportCustom) {

			// Write header
			$Doc->ExportTableHeader();
			if ($Doc->Horizontal) { // Horizontal format, write header
				$Doc->BeginExportRow();
				if ($ExportPageType == "view") {
					if ($this->gallery_id->Exportable) $Doc->ExportCaption($this->gallery_id);
					if ($this->model_id->Exportable) $Doc->ExportCaption($this->model_id);
					if ($this->image->Exportable) $Doc->ExportCaption($this->image);
					if ($this->img_order->Exportable) $Doc->ExportCaption($this->img_order);
					if ($this->status->Exportable) $Doc->ExportCaption($this->status);
				} else {
					if ($this->gallery_id->Exportable) $Doc->ExportCaption($this->gallery_id);
					if ($this->model_id->Exportable) $Doc->ExportCaption($this->model_id);
					if ($this->image->Exportable) $Doc->ExportCaption($this->image);
					if ($this->img_order->Exportable) $Doc->ExportCaption($this->img_order);
					if ($this->status->Exportable) $Doc->ExportCaption($this->status);
				}
				$Doc->EndExportRow();
			}
		}

		// Move to first record
		$RecCnt = $StartRec - 1;
		if (!$Recordset->EOF) {
			$Recordset->MoveFirst();
			if ($StartRec > 1)
				$Recordset->Move($StartRec - 1);
		}
		while (!$Recordset->EOF && $RecCnt < $StopRec) {
			$RecCnt++;
			if (intval($RecCnt) >= intval($StartRec)) {
				$RowCnt = intval($RecCnt) - intval($StartRec) + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($RowCnt > 1 && ($RowCnt - 1) % $this->ExportPageBreakCount == 0)
						$Doc->ExportPageBreak();
				}
				$this->LoadListRowValues($Recordset);

				// Render row
				$this->RowType = EW_ROWTYPE_VIEW; // Render view
				$this->ResetAttrs();
				$this->RenderListRow();
				if (!$Doc->ExportCustom) {
					$Doc->BeginExportRow($RowCnt); // Allow CSS styles if enabled
					if ($ExportPageType == "view") {
						if ($this->gallery_id->Exportable) $Doc->ExportField($this->gallery_id);
						if ($this->model_id->Exportable) $Doc->ExportField($this->model_id);
						if ($this->image->Exportable) $Doc->ExportField($this->image);
						if ($this->img_order->Exportable) $Doc->ExportField($this->img_order);
						if ($this->status->Exportable) $Doc->ExportField($this->status);
					} else {
						if ($this->gallery_id->Exportable) $Doc->ExportField($this->gallery_id);
						if ($this->model_id->Exportable) $Doc->ExportField($this->model_id);
						if ($this->image->Exportable) $Doc->ExportField($this->image);
						if ($this->img_order->Exportable) $Doc->ExportField($this->img_order);
						if ($this->status->Exportable) $Doc->ExportField($this->status);
					}
					$Doc->EndExportRow();
				}
			}

			// Call Row Export server event
			if ($Doc->ExportCustom)
				$this->Row_Export($Recordset->fields);
			$Recordset->MoveNext();
		}
		if (!$Doc->ExportCustom) {
			$Doc->ExportTableFooter();
		}
	}

