	// Export data in HTML/CSV/Word/Excel/Email/PDF format
	function ExportDocument(&$Doc, &$Recordset, $StartRec, $StopRec, $ExportPageType = "") {
		if (!$Recordset || !$Doc)
			return;

		// Write header
		$Doc->ExportTableHeader();
		if ($Doc->Horizontal) { // Horizontal format, write header
			$Doc->BeginExportRow();
			if ($ExportPageType == "view") {
				$Doc->ExportCaption($this->contact_id);
				$Doc->ExportCaption($this->full_name);
				$Doc->ExportCaption($this->_email);
				$Doc->ExportCaption($this->message);
				$Doc->ExportCaption($this->send_date);
				$Doc->ExportCaption($this->phone);
			} else {
				$Doc->ExportCaption($this->contact_id);
				$Doc->ExportCaption($this->full_name);
				$Doc->ExportCaption($this->_email);
				$Doc->ExportCaption($this->send_date);
				$Doc->ExportCaption($this->phone);
			}
			$Doc->EndExportRow();
		}

		// Move to first record
		$RecCnt = $StartRec - 1;
		if (!$Recordset->EOF) {
			$Recordset->MoveFirst();
			if ($StartRec > 1)
				$Recordset->Move($StartRec - 1);
		}
		while (!$Recordset->EOF && $RecCnt < $StopRec) {
			$RecCnt++;
			if (intval($RecCnt) >= intval($StartRec)) {
				$RowCnt = intval($RecCnt) - intval($StartRec) + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($RowCnt > 1 && ($RowCnt - 1) % $this->ExportPageBreakCount == 0)
						$Doc->ExportPageBreak();
				}
				$this->LoadListRowValues($Recordset);

				// Render row
				$this->RowType = EW_ROWTYPE_VIEW; // Render view
				$this->ResetAttrs();
				$this->RenderListRow();
				$Doc->BeginExportRow($RowCnt); // Allow CSS styles if enabled
				if ($ExportPageType == "view") {
					$Doc->ExportField($this->contact_id);
					$Doc->ExportField($this->full_name);
					$Doc->ExportField($this->_email);
					$Doc->ExportField($this->message);
					$Doc->ExportField($this->send_date);
					$Doc->ExportField($this->phone);
				} else {
					$Doc->ExportField($this->contact_id);
					$Doc->ExportField($this->full_name);
					$Doc->ExportField($this->_email);
					$Doc->ExportField($this->send_date);
					$Doc->ExportField($this->phone);
				}
				$Doc->EndExportRow();
			}
			$Recordset->MoveNext();
		}
		$Doc->ExportTableFooter();
	}

